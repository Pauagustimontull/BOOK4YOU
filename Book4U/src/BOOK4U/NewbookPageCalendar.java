    public void imagenes(Blob imagen) {
        Blob blob = imagen;
        byte[] imagenBytes = null;
        try {
            imagenBytes = blob.getBytes(1, (int) blob.length());
        } catch (SQLException e) {
            e.printStackTrace();
        }
        ImageIcon icono = new ImageIcon(imagenBytes);
        Barcelona1.setIcon(icono);
    }

